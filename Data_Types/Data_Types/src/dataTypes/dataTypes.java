package dataTypes;

public class dataTypes {
	public static void main(String[] args) {
		boolean c=true; // 1 bit, by default it is false;
		byte ab= 127; // 1 Byte
		char chr='B'; //2 Bytes. We know that all the values of the keyboard can be added in 1 Byte but still 
		//char has 2 Bytes so that it can support UNICode character set i.e. characters and symbols 
		// from various languages around the world.
		short i=10; // 2 Bytes
		int a=10;  //4 Bytes
		float mn=3.145f; // 4 Bytes
		double an= 3.125633d; //8 Bytes
		long ac=1234567890; // 8 Bytes
		
		
	}

} 
